import pandas as pd
import numpy as np

from kanly.api import lm

n = 5_000
n_cities = 5
cities = np.array(['Phi', 'NY', 'Bos', 'LA', 'SF', 'Tor', 'Sea'])

np.random.seed(0)
x = np.random.randn(n)
df = pd.DataFrame({'x': x,
                   'z': np.random.rand(n), 'w': np.random.randn(n),
                   'city': cities[np.random.randint(0, n_cities, n)],
                   # 'city': np.random.randint(0, n_cities, n),
                   'mean income': np.random.rand(n),
                   'zip': np.random.randint(0, 4, n)})

city_shifters = dict(zip(cities, np.random.randn(n)))

df['y'] = 1.2 + .5 * df.x - .12 * df.x ** 2 \
          - .6 * df.x * df.w + np.random.randn(n) \
          + df.city.map(city_shifters) \
          + .05 * df['mean income']


fit = lm('y ~ poly(x,2):C(city) + poly(x,2,-1):C(zip) + C(city):C(zip)'
         ' + w + Q("mean income") + np.log(z+6)', df)

print(fit)

