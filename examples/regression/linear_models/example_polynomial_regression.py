import numpy as np
import pandas as pd

from kanly.api import lm

n = 1_500
np.random.seed(0)

df = pd.DataFrame({
    'x': np.random.randn(n),
    'z': np.random.randn(n),
    'w': np.random.randn(n),
})

df['y'] = 1.2 - 0.3 * df['x'] + .185 * df['x'] * df['z'] \
          - .08 * df['w'] ** 2 + 1.2 * df['w'] ** 3 \
          + 1.5 * np.random.randn(n)

fit = lm('y ~ poly(x, 2) * poly(z, 2) + poly(w, [2,3]) * x', df, debug=False)
print(fit)


